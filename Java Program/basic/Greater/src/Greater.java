
public class Greater {

	public static void main(String[] args) {
		int a=1,b=2,c=3;
		
		if(a>=b && a>=c) 
		{
		System.out.println("Largest number: "+a);
		}
		else if(b>=a && b>=c) 
		{
		System.out.println("Largest number: "+b);
		}
		else if(c>=a && c>=b) 
		{
		System.out.println("Largest number: "+c);
		}

	}

}


